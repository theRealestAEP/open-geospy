"""Tests for eval manifest parsing and distance math in eval/common.py."""

import csv
import os

import pytest

from eval.common import EvalCase, cases_to_rows, haversine_m, load_eval_cases, write_csv

CSV_HEADER = (
    "case_id,image_path,expected_lat,expected_lon,"
    "expected_panorama_id,expected_capture_id,expected_reject,split,notes"
)


class TestHaversine:
    def test_zero_distance(self):
        assert haversine_m(37.0, -122.0, 37.0, -122.0) < 0.01

    def test_one_degree_latitude(self):
        assert haversine_m(0.0, 0.0, 1.0, 0.0) == pytest.approx(111195, rel=0.001)


class TestLoadEvalCases:
    def write_manifest(self, tmp_path, lines):
        path = tmp_path / "manifest.csv"
        path.write_text("\n".join([CSV_HEADER, *lines]) + "\n", encoding="utf-8")
        return str(path)

    def test_full_row_parsed(self, tmp_path):
        path = self.write_manifest(
            tmp_path,
            ["case-a,img.jpg,37.77,-122.41,55,99,true,test,hello"],
        )
        cases = load_eval_cases(path)
        assert len(cases) == 1
        case = cases[0]
        assert case.case_id == "case-a"
        # Relative image paths resolve against the manifest directory.
        assert case.image_path == os.path.join(str(tmp_path), "img.jpg")
        assert case.expected_lat == pytest.approx(37.77)
        assert case.expected_lon == pytest.approx(-122.41)
        assert case.expected_panorama_id == 55
        assert case.expected_capture_id == 99
        assert case.expected_reject is True
        assert case.split == "test"
        assert case.notes == "hello"

    def test_absolute_path_kept(self, tmp_path):
        path = self.write_manifest(tmp_path, ["c1,/abs/img.jpg,,,,,,,"])
        cases = load_eval_cases(path)
        assert cases[0].image_path == "/abs/img.jpg"

    def test_optional_fields_default_to_none(self, tmp_path):
        path = self.write_manifest(tmp_path, ["c1,img.jpg,,,,,,,"])
        case = load_eval_cases(path)[0]
        assert case.expected_lat is None
        assert case.expected_lon is None
        assert case.expected_panorama_id is None
        assert case.expected_capture_id is None
        assert case.expected_reject is False
        assert case.split == "dev"

    def test_missing_case_id_autogenerated(self, tmp_path):
        path = self.write_manifest(tmp_path, [",img.jpg,,,,,,,", ",img2.jpg,,,,,,,"])
        cases = load_eval_cases(path)
        assert [c.case_id for c in cases] == ["case-1", "case-2"]

    def test_row_without_image_path_skipped(self, tmp_path):
        path = self.write_manifest(tmp_path, ["c1,,,,,,,,", "c2,img.jpg,,,,,,,"])
        cases = load_eval_cases(path)
        assert [c.case_id for c in cases] == ["c2"]

    def test_reject_bool_variants(self, tmp_path):
        rows = [
            f"c{i},img.jpg,,,,,{raw},,"
            for i, raw in enumerate(["1", "yes", "on", "false", "", "nope"])
        ]
        path = self.write_manifest(tmp_path, rows)
        flags = [c.expected_reject for c in load_eval_cases(path)]
        assert flags == [True, True, True, False, False, False]


class TestWriteCsv:
    def test_union_of_keys_sorted(self, tmp_path):
        path = str(tmp_path / "out.csv")
        write_csv(path, [{"b": 1, "a": 2}, {"c": 3}])
        with open(path, newline="") as f:
            rows = list(csv.DictReader(f))
        assert list(rows[0].keys()) == ["a", "b", "c"]
        assert rows[0]["a"] == "2"
        assert rows[1]["c"] == "3"

    def test_empty_rows_write_nothing(self, tmp_path):
        path = str(tmp_path / "out.csv")
        write_csv(path, [])
        assert not os.path.exists(path)


